     private void AfficherTable() {        
        List<Filiere> filieres = retourneFilieres(); 

        DefaultTableModel model = new DefaultTableModel();

        model.addColumn("N°");
        model.addColumn("Nom De La Filière ");
        model.addColumn("Departement d'appartenance ");

        for (Filiere filiere : filieres){
            
            Departement departementCorrespondant = retourneDepartement(filiere.getIdDepart().getIdDepartement());
            
            Object[] rowData = {filiere.getIdFiliere(), filiere.getNomFiliere(), departementCorrespondant.getNomDepartement()};
            model.addRow(rowData);
        }
        // Rafraîchir la table après avoir défini le nouveau modèle
//        ((DefaultTableModel) FiliereTable.getModel()).fireTableDataChanged();

        FiliereTable.setModel(model);
        
    }
    
    private void SelectionnerLigne() {
        FiliereTable.getSelectionModel().addListSelectionListener((ListSelectionEvent e) -> {
            if (!e.getValueIsAdjusting()) {
                int selectedRow = FiliereTable.getSelectedRow();

                System.out.println(selectedRow);
                if (selectedRow != -1) {
                    // Récupérer les éléments de la ligne sélectionnée
                    int id = Integer.parseInt(FiliereTable.getValueAt(selectedRow, 0).toString());
                    String nom = FiliereTable.getValueAt(selectedRow, 1).toString();
                    String departementName = FiliereTable.getValueAt(selectedRow, 2).toString();
                    
                    Departement departement = retourneDepartementByName(departementName);
                    
                    // Afficher les éléments récupérés dans le formulaire
                    FilièreNameField.setText(nom);
                    filiereDepartComboBox.setSelectedIndex(departement.getIdDepartement() - 1);
                    
                    idSelectedLigne = id;
                }
            }
        });   
    }
    
    public int getIdSelected() {
        return idSelectedLigne;
    }
    
    public void ResetForm() {
        FilièreNameField.setText("");
        filiereDepartComboBox.setSelectedIndex(0);
    }
    
    
    private void Recherche() {
        TableRowSorter<TableModel> sort = new TableRowSorter<>(FiliereTable.getModel());
        FiliereTable.setRowSorter(sort);
        
        searchField.getDocument().addDocumentListener(new DocumentListener() {
            @Override
            public void insertUpdate(DocumentEvent e) {
                String str = searchField.getText();
                if (str.trim().length() == 0) {
                    sort.setRowFilter(null);
                } else {
                    // Échappez les caractères spéciaux dans la chaîne de recherche
                    String escapedStr = java.util.regex.Pattern.quote(str);
                    sort.setRowFilter(RowFilter.regexFilter("(?i)" + escapedStr));
                }
            }

            @Override
            public void removeUpdate(DocumentEvent e) {
                String str = searchField.getText();
                if (str.trim().length() == 0) {
                    sort.setRowFilter(null);
                } else {
                    // Échappez les caractères spéciaux dans la chaîne de recherche
                    String escapedStr = java.util.regex.Pattern.quote(str);
                    sort.setRowFilter(RowFilter.regexFilter("(?i)" + escapedStr));
                }
            }

            @Override
            public void changedUpdate(DocumentEvent e) {
                
            }
        
        });
    }
    
    private void LoadComboBox() {
        List<Departement> departements = retourneDepartements();
        
        for( Departement departement : departements) {
            filiereDepartComboBox.addItem(departement.getNomDepartement());
        }
    }
    
    /**
     * Représente l'entity manager factory (unité de persistance)
     */
    private final EntityManagerFactory emf = javax.persistence.Persistence.createEntityManagerFactory("GestionNotesPU");
    /**
     * Représente l'entity manager pour effectuer les opérations sur les bases
     * de données
     */
    private final EntityManager entitymanager = emf.createEntityManager();
	
    /**
     * Retourne la liste des Formations
     * @return liste des formation
     */
public List<Filiere> retourneFilieres() {
       
        
        Query jpql = entitymanager.createNamedQuery("Filiere.findAll");

        return jpql.getResultList();
    }

       /**
     * Retourne la filiere
     * @param id
     * @return
     */
    public Filiere retourneFiliere(int id) {
            // Utilisation de la requête nommée "Filiere.findById"
            Query query = entitymanager.createNamedQuery("Filiere.findByIdFiliere");
            query.setParameter("idFiliere", id);

            // Exécution de la requête
            return (Filiere) query.getSingleResult();
    }
    
        /**
     * Retourne la liste des Departements
     * @return liste des Departements
     */
    public List<Departement> retourneDepartements() {
        //Query jpql = entitymanager.createQuery("SELECT e FROM Departement e");
        Query jpql = entitymanager.createNamedQuery("Departement.findAll");

        return jpql.getResultList();
    }
    
    /**
     * Retourne le departement
     * @param id
     * @return
     */
    public Departement retourneDepartement(int id) {
            // Utilisation de la requête nommée "Departement.findById"
            Query query = entitymanager.createNamedQuery("Departement.findByIdDepartement");
            query.setParameter("idDepartement", id);

            // Exécution de la requête
            return (Departement) query.getSingleResult();
    }
    
        /**
     * Retourne le departement
     * @param nom
     * @return
     */
    public Departement retourneDepartementByName(String nom) {
            Query query = entitymanager.createNamedQuery("Departement.findByNomDepartement");
            query.setParameter("nomDepartement", nom);

            // Exécution de la requête
            return (Departement) query.getSingleResult();
    }

    /**
     * Enrégistrer une filiere
     *
     * @param filiere
     */
    public void enregistrerFiliere(Filiere filiere) {
        entitymanager.getTransaction().begin();
        entitymanager.persist(filiere);
        entitymanager.getTransaction().commit();
      
    }
    
    public void UpdateFiliere(Filiere filiere) {
        entitymanager.getTransaction().begin();
        entitymanager.merge(filiere);
        entitymanager.getTransaction().commit();
    }
    
    public void DeleteFiliere(Filiere filiere) {
        entitymanager.getTransaction().begin();
        entitymanager.remove(filiere);
        entitymanager.getTransaction().commit();
    }
    
    
